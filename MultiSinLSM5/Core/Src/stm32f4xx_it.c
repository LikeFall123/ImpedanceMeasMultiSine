/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */
  extern uint8_t sinBuffer[2*NUM_OF_SAMPLES_DAC];
  extern volatile uint8_t sinBufferTemp[3];
  extern volatile uint16_t sinCounter;
  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */
  HAL_GPIO_WritePin(SPI_SW_NSS_GPIO_Port, SPI_SW_NSS_Pin, GPIO_PIN_RESET);
  if (sinCounter >= 2*NUM_OF_SAMPLES_DAC)
  {
		  sinCounter=0;
  }
   sinBufferTemp[1] = sinBuffer[sinCounter];
   sinBufferTemp[2] = sinBuffer[sinCounter+1];
   HAL_SPI_Transmit_IT(&hspi1,(uint8_t*)(&sinBufferTemp), 3);
   sinCounter = sinCounter+2;
  /* USER CODE END TIM2_IRQn 1 */
}

/**
  * @brief This function handles SPI1 global interrupt.
  */
